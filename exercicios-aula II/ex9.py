# Exercício 9 - Média Aritmética

n1 = float(input("Digite nota do seu primeiro bimestre: "))
n2 = float(input("Digite nota do seu segundo bimestre:: "))
n3 = float(input("Digite nota do seu terceiro bimestre:: "))
n4 = float(input("Digite nota do seu quarto bimestre:: "))

mediaAritmetica = (n1 + n2 + n3 + n4) / 4

print(f"Sua média aritmética foi de {mediaAritmetica:.2f} pontos")